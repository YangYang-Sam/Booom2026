        public static double Sqrt1(double d)
        {
            bool sign = d > 0;
            d *= sign ? 1 : -1;

            int head = 0;
            int tail = perfectSquares.Length - 1;
            int mid = tail / 2;

            // Find a initial approximation
            double approx = 0;
            if (d <= perfectSquares[tail])
            {
                while (tail - head > 1)
                {
                    if (perfectSquares[mid] < d)
                    {
                        head = mid;
                    }
                    else
                    {
                        tail = mid;
                    }
                    mid = (tail - head) / 2 + head;
                }
                if (perfectSquares[head] == d) return sign ? head : -head;
                if (perfectSquares[tail] == d) return sign ? tail : -tail;
                approx = (d - perfectSquares[head]) / (perfectSquares[tail] - perfectSquares[head]) + head;
            }
            else
            {
                head = tail;
                tail = (int)(d / perfectSquares[tail]);
                mid = (tail - head) / 2 + head;

                while (tail - head > 1)
                {
                    if (mid * mid < d)
                    {
                        head = mid;
                    }
                    else
                    {
                        tail = mid;
                    }
                    mid = (tail - head) / 2 + head;
                }

                if (mid * mid == d) return sign ? mid : -mid;
                approx = mid;
            }

            double last = -1;
            double diff = 1;

            while (diff > dAdvStep)
            {
                last = approx;
                approx = (d / approx + approx) * 0.5;

                diff = approx - last;
                diff *= diff < 0 ? -1 : 1;
            }

            return sign ? approx : -approx;
        }

        /// <summary>
        /// Find the cubic root of an number, a bit faster than Cbrt2
        /// </summary>
        /// <param name="d"></param>
        /// <returns></returns>
        /// <remarks>
        /// Halley's method
        /// https://en.wikipedia.org/wiki/Cube_root
        /// </remarks>
        public static double Cbrt1(double d)
        {
            bool sign = d > 0;
            d *= sign ? 1 : -1;

            int head = 0;
            int tail = perfectCubics.Length - 1;
            int mid = tail / 2;

            // Find a initial approximation
            double approx = 0;
            if (d <= perfectCubics[tail])
            {
                while (tail - head > 1)
                {
                    if (perfectCubics[mid] < d)
                    {
                        head = mid;
                    }
                    else
                    {
                        tail = mid;
                    }
                    mid = (tail - head) / 2 + head;
                }

                if (perfectCubics[head] == d) return sign ? head : -head;
                if (perfectCubics[tail] == d) return sign ? tail : -tail;
                approx = (d - perfectCubics[head]) / (perfectCubics[tail] - perfectCubics[head]) + head;
            }
            else
            {
                head = tail;
                tail = (int)((d / perfectCubics[tail]) / perfectCubics[tail]);
                mid = (tail - head) / 2 + head;

                while (tail - head > 1)
                {
                    if (mid * mid * mid < d)
                    {
                        head = mid;
                    }
                    else
                    {
                        tail = mid;
                    }
                    mid = (tail - head) / 2 + head;
                }

                if (mid * mid * mid == d) return sign ? mid : -mid;
                approx = mid;
            }

            // Iteration
            double last = -1;
            double diff = 1;
            double approx3;
            while (diff > dAdvStep)
            {
                last = approx;
                approx3 = approx * approx * approx;
                approx = approx * (approx3 + 2 * d) / (2 * approx3 + d);

                diff = approx - last;
                diff *= diff < 0 ? -1 : 1;
            }

            return sign ? approx : -approx;
        }

        /// <summary>
        /// Find the cubic root of an number, a tiny bit more accurate than Cbrt1
        /// </summary>
        /// <param name="d"></param>
        /// <returns></returns>
        /// <remarks>
        /// Newton's method
        /// https://en.wikipedia.org/wiki/Cube_root
        /// </remarks>
        public static double Cbrt2(double d)
        {
            bool sign = d > 0;
            d *= sign ? 1 : -1;

            int head = 0;
            int tail = perfectCubics.Length - 1;
            int mid = tail / 2;

            // Find a initial approximation
            double approx = 0;
            if (d <= perfectCubics[tail])
            {
                while (tail - head > 1)
                {
                    if (perfectCubics[mid] < d)
                    {
                        head = mid;
                    }
                    else
                    {
                        tail = mid;
                    }
                    mid = (tail - head) / 2 + head;
                }

                if (perfectCubics[head] == d) return sign ? head : -head;
                if (perfectCubics[tail] == d) return sign ? tail : -tail;

                approx = (d - perfectCubics[head]) / (perfectCubics[tail] - perfectCubics[head]) + head;
            }
            else
            {
                head = tail;
                tail = (int)((d / perfectCubics[tail]) / perfectCubics[tail]);
                mid = (tail - head) / 2 + head;

                while (tail - head > 1)
                {
                    if (mid * mid * mid < d)
                    {
                        head = mid;
                    }
                    else
                    {
                        tail = mid;
                    }
                    mid = (tail - head) / 2 + head;
                }

                if (mid * mid * mid == d) return sign ? mid : -mid;
                approx = mid;
            }

            // Iteration
            double last = -1;
            double diff = 1;
            while (diff > dAdvStep)
            {
                last = approx;
                approx = (d / (approx * approx) + 2 * approx) / 3;

                diff = approx - last;
                diff *= diff < 0 ? -1 : 1;
            }

            return sign ? approx : -approx;
        }

