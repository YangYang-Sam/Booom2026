        public static float OverlappingAreaBetweenCircleAndSquare_Monte_Carlo(Vector2 cCenter, float cRadius, Vector2 sCenter, Vector2 sSize, int sample = 10000)
        {
            float cSqrRadius = cRadius * cRadius;
            float cArea = cRadius * cRadius * Mathf.PI;
            float sArea = sSize.x * sSize.y;
            sCenter -= cCenter;
            // To Extend
            sSize *= 0.5f;

            // If circle completely covers square
            if (cSqrRadius >= (sCenter + sSize).sqrMagnitude &&
                cSqrRadius >= (sCenter - sSize).sqrMagnitude &&
                cSqrRadius >= (sCenter + sSize.AlterComponent(0, sSize.x * -1)).sqrMagnitude &&
                cSqrRadius >= (sCenter + sSize.AlterComponent(1, sSize.y * -1)).sqrMagnitude)
            {
                return sArea;
            }

            Vector2
                maxCorner = sCenter + sSize,
                minCorner = sCenter - sSize;

            // If square completely covers circle
            if (cRadius < maxCorner.x &&
                cRadius < -minCorner.x &&
                cRadius < maxCorner.y &&
                cRadius < -minCorner.y)
            {
                return cArea;
            }

            float percent = 0f;
            Vector2 samplePoint;
            for (int i = 0; i < sample; i++)
            {
                samplePoint = UnityEngine.Random.insideUnitCircle * cRadius;
                if (samplePoint.x <= maxCorner.x &&
                    samplePoint.x >= minCorner.x &&
                    samplePoint.y <= maxCorner.y &&
                    samplePoint.y >= minCorner.y)
                    percent++;
            }

            return cArea * (percent / sample);
        }

        /// <summary>
        /// Solve standard quadratic formula(no complex number roots)
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <param name="c"></param>
        /// <param name="answers"></param>
        public static void SolveQuadraticEquation(double a, double b, double c, out double[] answers)
        {
            answers = new double[2];
            if (a == 0)
            {
                answers[0] = c / -b;
                answers[1] = double.NaN;
                return;
            }

            var discriminant = b * b - 4 * a * c;

            if (discriminant < 0)
            {
                answers[0] = double.NaN;
                answers[1] = double.NaN;
                return;
            }

            discriminant = Math.Sqrt(discriminant);

            answers[0] = (-b + discriminant) / (2 * a);
            answers[1] = (-b - discriminant) / (2 * a);

            if (answers[0] == double.NaN)
            {
                answers[0] = answers[1];
                answers[1] = double.NaN;
            }
        }

